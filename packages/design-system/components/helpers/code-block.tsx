'use client';

import { useEffect, useState } from 'react';
import { createHighlighter, type Highlighter } from 'shiki';

interface CodeBlockProps {
  code: string;
  language?: string;
  showLineNumbers?: boolean;
}

export function CodeBlock({
  code,
  language = 'typescript',
  showLineNumbers = false,
}: CodeBlockProps) {
  const [highlightedCode, setHighlightedCode] = useState<string>('');
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    let highlighter: Highlighter | null = null;

    async function highlight() {
      try {
        highlighter = await createHighlighter({
          themes: ['github-dark', 'github-light'],
          langs: ['typescript', 'tsx', 'javascript', 'jsx', 'json', 'css', 'html'],
        });

        const html = highlighter.codeToHtml(code, {
          lang: language,
          themes: {
            light: 'github-light',
            dark: 'github-dark',
          },
        });

        // Add custom classes to the <pre> tag generated by Shiki
        // Remove Shiki's inline background-color style and add our classes
        let styledHtml = html.replace(
          '<pre class="shiki',
          '<pre class="shiki bg-muted rounded-lg p-lg overflow-x-auto'
        );

        // Remove inline background-color style from Shiki
        styledHtml = styledHtml.replace(/style="[^"]*background-color:[^;"]*;?/g, 'style="');
        styledHtml = styledHtml.replace(/style=""\s*/g, '');

        setHighlightedCode(styledHtml);
        setIsLoading(false);
      } catch (error) {
        console.error('Failed to highlight code:', error);
        setIsLoading(false);
      }
    }

    highlight();

    return () => {
      if (highlighter) {
        highlighter.dispose();
      }
    };
  }, [code, language]);

  if (isLoading) {
    return (
      <pre className="bg-muted rounded-lg p-lg overflow-x-auto text-sm">
        <code className="font-mono">{code}</code>
      </pre>
    );
  }

  return (
    <div
      className="shiki-wrapper"
      dangerouslySetInnerHTML={{ __html: highlightedCode }}
    />
  );
}
